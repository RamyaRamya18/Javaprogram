public class CharOfArray {
    public static void main(String[] args) {
        char []ch = {'j','a','v','a','c','o','d','e'};
        for(int i=0;i< ch.length;i++){
            System.out.print("print the char of array :");
            System.out.println(ch[i]+" ");
        }
    }
}


